/**
 * Choose locale for the request. Order:
 *   1. ?lang=tr|en query param (explicit override)
 *   2. req.user.locale (when auth middleware exposes it — not wired yet)
 *   3. Accept-Language header — `tr*` -> tr, anything else -> en
 *   4. Default 'en'
 */
export function detectExportLocale(req: Request): ExportLocale {
  const q = String((req.query as Record<string, unknown>)?.lang ?? '').toLowerCase();
  if (q === 'tr' || q === 'en') return q;

  const userLocale = (req as Request & { user?: { locale?: string } }).user?.locale;
  if (userLocale === 'tr' || userLocale === 'en') return userLocale;

  const al = String(req.headers['accept-language'] ?? '').toLowerCase();
  if (al.startsWith('tr')) return 'tr';
  return 'en';
}
